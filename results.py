results = ["Jack", "Stacy", "James"]

results.append("Leon") # adds one item to the end of the list

results.extend(["Olivia", "Hike"]) # adds more than one item at the end of the list

results.remove("Stacy") # deletes items from a list

results.insert(1, "Stacy") # add items in a list at a specific index

results.reverse() # reverses the list and the last item becomes the first item ...

print(results)